package com.udemy.application1;

public class Course {

	
	private long id;
	private String name;
	private String address;
	
	
	
	
	
	public Course(long id, String name, String address) {
		super();
		this.id = id;
		this.name = name;
		this.address = address;
	}
	public long getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getAddress() {
		return address;
	}
	
	
	
	
	
}
